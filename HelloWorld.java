package ch.bbw.Do.cluedo;

public class HelloWorld {

	private int i;

	private String text;

	public HelloWorld()
	{
		this.i = 0;
		this.text = "Hallo";
	}

	public int getI()
	{
		return this.i;
	}

	public void setI(int i)
	{
		this.i = i;
	}

	public void setText(String text)
	{
		this.text = text;
	}

	public String getText()
	{
		return this.text;
	}

}
